using FacebookAPI___PageAPI.services;

var builder = WebApplication.CreateBuilder(args);

builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddHttpClient<FacebookService>();
builder.Services.AddSingleton<FacebookAPI___PageAPI.Data.BackendStateRepository>();

// Register background Kafka consumer worker
builder.Services.AddHostedService<FacebookAPI___PageAPI.Services.KafkaReplyConsumerWorker>();

builder.Services.AddCors(options =>
{
    options.AddPolicy("AllowAll", policy =>
    {
        policy.AllowAnyOrigin()
              .AllowAnyHeader()
              .AllowAnyMethod();
    });
});

var app = builder.Build();

app.UseSwagger();
app.UseSwaggerUI();

app.UseCors("AllowAll");

app.UseAuthorization();

app.MapControllers();

app.Run();